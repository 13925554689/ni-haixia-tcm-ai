def formula_diagnose(symptoms: str) -> Optional[dict]:
    """公式优先辨证：六经速查表匹配 → 返回结构化辨证结果
    返回 None 表示公式无法匹配，应降级到关键词引擎
    """
    # 先跑快速查表
    for pattern, channel, formula, confidence, tip in QUICK_LOOKUP:
        if re.search(pattern, symptoms):
            formula_detail = SIX_CHANNEL_FORMULAS.get(channel, {})
            return {
                "六经定位": channel,
                "主方": formula,
                "置信度": confidence,
                "辨证公式": formula_detail.get("公式", ""),
                "辨证法则": formula_detail.get("辨证法则", ""),
                "鉴别提示": tip,
                "鉴别要点": formula_detail.get("鉴别要点", [])[:3],
                "危险信号": formula_detail.get("危险信号", ""),
                "引擎": "formula",
            }

    # 快速查表未命中 → 尝试六经公式逐经匹配
    best_match = None
    best_score = 0

    channel_keywords = {
        "太阳病": ["脉浮","头项强","恶寒","恶风","有汗","无汗","项背强","鼻鸣","干呕","身痛"],
        "阳明病": ["大热","大汗","大渴","便秘","腹满","谵语","潮热","心中懊","手足濈然","日晡","白虎","承气"],
        "少阳病": ["口苦","咽干","目眩","胸胁","往来寒热","默默不欲食","心烦喜呕","弦脉"],
        "太阴病": ["腹满时痛","食不下","下利清","四肢不温","吐利"],
        "少阴病": ["脉微细","欲寐","四肢厥","手脚冰冷","心中烦不得卧","但欲寐"],
        "厥阴病": ["消渴","气上撞","心中疼","饥不欲食","手足厥寒","热利下重"],
    }

    for channel, keywords in channel_keywords.items():
        score = sum(1 for kw in keywords if kw in symptoms)
        if score > best_score:
            best_score = score
            best_match = channel

    if best_match and best_score >= 2:
        formula_detail = SIX_CHANNEL_FORMULAS.get(best_match, {})
        return {
            "六经定位": best_match,
            "主方": "辨证选方中",
            "置信度": "中" if best_score >= 3 else "低",
            "辨证公式": formula_detail.get("公式", ""),
            "辨证法则": formula_detail.get("辨证法则", ""),
            "鉴别要点": formula_detail.get("鉴别要点", [])[:3],
            "危险信号": formula_detail.get("危险信号", ""),
            "引擎": "formula_partial",
        }

    return None  # 公式无法匹配


def enrich_diagnosis_with_formula(diag: dict) -> dict:
    """用公式数据增强已有辨证结果"""
    channel = diag.get("六经定位", "")
    if channel in SIX_CHANNEL_FORMULAS:
        fd = SIX_CHANNEL_FORMULAS[channel]
        diag["辨证公式"] = fd.get("公式", "")
        diag["辨证法则"] = fd.get("辨证法则", "")
        diag["鉴别要点"] = fd.get("鉴别要点", [])[:3]
        diag["危险信号"] = fd.get("危险信号", "")

    return diag


def get_quick_table() -> List[dict]:
    """返回用于调试/展示的快速查表"""
    return [
        {"pattern": p, "channel": c, "formula": f, "confidence": conf, "tip": t}
        for p, c, f, conf, t in QUICK_LOOKUP
    ]


def get_all_formulas() -> dict:
    """返回全部六经诊断公式"""
    return SIX_CHANNEL_FORMULAS
